# Задайте последовательность чисел. Напишите программу, которая выведет список неповторяющихся элементов исходной
# последовательности.

def unique_list(my_list: list) -> list:
    return list(set(my_list))


test_date = [
    [[1, 2, 3, 2, 5, 5, 7, 8], [1, 2, 3, 5, 7, 8]],
    [[1, 1, 1, 1], [1]],
    [[1, 2, 3, 3, 2, 1], [1, 2, 3]]
]

for i, j in test_date:
    assert unique_list(i) == j